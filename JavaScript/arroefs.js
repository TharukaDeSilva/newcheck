// Arrow function to check if a number is prime
const isPrime = (num) => {
  if (num <= 1) {
    return false;
  }
  for (let i = 2; i <= Math.sqrt(num); i++) {
    if (num % i === 0) {
      return false;
    }
  }
  return true;
};

// Function to get prime numbers up to a given limit
const getPrimesUpTo = (limit) => {
  const primes = [];
  for (let i = 2; i <= limit; i++) {
    if (isPrime(i)) {
      primes.push(i);
    }
  }
  return primes;
};

console.log("Prime numbers up to 100:", getPrimesUpTo(100));
